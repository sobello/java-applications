package com.session.third;

public class CalculatorOperations {
	public static int doAdd(int a, int b) {
		return a + b;
	}

	public static int doSubtract(int a, int b) {
		return a - b;
	}
	
	public static int doMultiply(int a, int b) {
		return a * b;
	}
	
	public static double doDivide(int a, int b) {
		return (double)a / b;
	}
}